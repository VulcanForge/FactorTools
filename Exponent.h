#pragma once

#include <cstdint>

/// <summary>
/// Returns a base to the power of an exponent.
/// </summary>
/// <param name="base">The base.</param>
/// <param name="exponent">The exponent.</param>
uint64_t Pow (uint64_t base, uint64_t exponent);

/// <summary>
/// Returns a base to the power of an exponent.
/// </summary>
/// <param name="base">The base.</param>
/// <param name="exponent">The exponent.</param>
double Pow (double base, uint64_t exponent);
